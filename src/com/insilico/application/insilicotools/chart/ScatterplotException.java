package com.insilico.application.insilicotools.chart;

public class ScatterplotException extends Exception {

    public ScatterplotException(String message) {
        super(message);
    }

    public ScatterplotException(Throwable cause) {
        super(cause);
    }

    public ScatterplotException(String message, Throwable cause) {
        super(message, cause);
    }
}