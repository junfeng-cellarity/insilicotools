package com.insilico.application.insilicotools.chart;

public class ScatterplotRuntimeException extends RuntimeException {

    public ScatterplotRuntimeException(String message) {
        super(message);
    }

    public ScatterplotRuntimeException(Throwable cause) {
        super(cause);
    }

    public ScatterplotRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }
}